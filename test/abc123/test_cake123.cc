#include "gtest/gtest.h"
#include <vector>
#include "abc123/cake123.h"

using namespace std;

TEST(TestCake123, First) {
  long long a[] = {4, 6};
  long long b[] = {1, 5};
  long long c[] = {3, 8};
  vector<long long> ans = {19, 17, 15, 14, 13, 12, 10, 8};
  EXPECT_EQ(ans, cake123(2, 2, 2, 8, a, b, c));
}

